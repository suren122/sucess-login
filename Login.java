    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String emails= email.getText();
        String passwords= password.getText();
        String roles= (String) role.getSelectedItem();
        if(email.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out email");
            return; 
            
        }
        else if(password.getText().equals("")){
            JOptionPane.showMessageDialog(null," please fill out password");
            return; 
        }
        
        String emailInput = email.getText();
        String passwordInput = password.getText();

        // Define regex patterns for email and password
        String emailPattern = "^[a-zA-Z0-9]+@gmail\\.com$";
        String passwordPattern = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z]).{8,}$";

        // Check if email and password match the regex patterns
        if (Pattern.matches(emailPattern, emailInput) && Pattern.matches(passwordPattern, passwordInput)) {
              Connection conn;
              PreparedStatement preparedStatement = null;
              try {
                  String URL = "jdbc:mysql://localhost:3306/CMS";
                  String PASSWORD = "";
                  String USERNAME = "root";
                  conn = DriverManager.getConnection(URL, USERNAME, PASSWORD);
                  String q = "SELECT * FROM " + roles + " WHERE emails = ? AND passwords = ?";

                  // Use PreparedStatement to avoid SQL injection
                  preparedStatement = conn.prepareStatement(q);
                  preparedStatement.setString(1, emailInput);
                  preparedStatement.setString(2, passwordInput);

                  // Use executeQuery for SELECT statements
                  ResultSet result = preparedStatement.executeQuery();
   
                  if (result.next()) {
                     System.out.println("LOGIN SUCCESSFUL");
                     JOptionPane.showMessageDialog(null, "Login successful", "Success", JOptionPane.INFORMATION_MESSAGE);
                  } else {
                     JOptionPane.showMessageDialog(null, "Invalid email or password", "Error", JOptionPane.ERROR_MESSAGE);
                  }

                  } catch (SQLException ee) {
                      System.out.println("Connection Failed with the Database: " + ee);
                      JOptionPane.showMessageDialog(null, "Error connecting to the database", "Error", JOptionPane.ERROR_MESSAGE);
} 

            
                   } else {
                    JOptionPane.showMessageDialog(null, "Invalid email or password format", "Error", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_loginActionPerformed

